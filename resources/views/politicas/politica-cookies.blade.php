  <h3 class="text-xl font-semibold text-gray-800 mt-10 mb-4">Definición y función de las cookies</h3>
  <p class="mb-4">Las cookies son informaciones que se almacenan en el navegador del usuario de un sitio web para poder consultar la actividad previa que ha tenido, así como recordar ciertos datos para una próxima visita. También pueden ser llamadas web beacons, píxel, bugs, rastreadores, pero a efectos de estas políticas, se entenderán solamente como <strong>cookies</strong>.</p>
  <p class="mb-4">Suelen almacenar datos de carácter técnico, estadísticas de uso, personalización de perfiles, enlaces a redes sociales, administración de preferencias personales, entre otras funciones, con el fin de poder adaptar el sitio web a las necesidades y configuraciones del usuario, mejorando así la experiencia de la navegación, el no aceptarlas podría entorpecer el servicio que el sitio web desea ofrecer.</p>
  <p class="mb-4">No son archivos de virus, spam, troyanos, gusanos, spyware, ni programación publicitaria ya sea estática en la página o en formato de ventana emergente (pop-up).</p>
  <p class="mb-4">La información almacenada se refiere al navegador (Internet Explorer, Safari, Chrome, Firefox, etc.), y no al usuario, para constatar esto puede entrar en el mismo dominio web y verificar en dos navegadores diferentes que puede configurar diferentes preferencias en cada uno.</p>

  <h3 class="text-xl font-semibold text-gray-800 mt-10 mb-4">Tipos de cookies según la entidad que las gestiona</h3>
  <p class="mb-2">Las cookies pueden ser:</p>
  <ol class="list-decimal list-inside mb-4 space-y-2">
    <li><strong>Propias:</strong> cuando son gestionadas por el propio editor del sitio web. Se envían al equipo terminal del usuario desde un equipo o dominio gestionado por el propio editor y desde el que se presta el servicio solicitado por el usuario.</li>
    <li><strong>De terceros:</strong> cuando son gestionadas por una personas física o jurídica diferente al editor.</li>
  </ol>
  <p class="mb-4">Este sitio web utiliza cookies propias y de terceros.</p>

  <h3 class="text-xl font-semibold text-gray-800 mt-10 mb-4">Tipos de cookies según su finalidad</h3>
  <p class="mb-2">Dependiendo de la función que cumplan, las cookies pueden ser:</p>
  <ol class="list-decimal list-inside mb-4 space-y-2">
    <li><strong>Técnicas:</strong> Permiten la gestión operativa del sitio web...</li>
    <li><strong>De preferencias o personalización:</strong> son aquellas que permiten recordar información para que el usuario navegue en el sitio web con determinadas características personalizadas...</li>
    <li><strong>De análisis o medición:</strong> permiten el seguimiento y análisis del comportamiento de los usuarios en el sitio web...</li>
    <li><strong>De publicidad comportamental:</strong> son aquellas que almacenan información del comportamiento de los usuarios obtenida a través de la observación continuada...</li>
    <li><strong>Publicitarias:</strong> Son aquéllas que permiten la gestión, de la forma más eficaz posible, de los espacios publicitarios...</li>
  </ol>
  <p class="mb-4">Este sitio web utiliza cookies técnicas, de personalización, de análisis, de publicidad y de publicidad comportamental.</p>
  <p class="mb-4">Algunas cookies pueden ser polivalentes, es decir, cumplir con 2 o más funciones.</p>

  <h3 class="text-xl font-semibold text-gray-800 mt-10 mb-4">Tipos de cookies según su duración:</h3>
  <p class="mb-2">Según el plazo de tiempo que permanecen activadas en el equipo terminal, las cookies pueden ser:</p>
  <ol class="list-decimal list-inside mb-4 space-y-2">
    <li><strong>De sesión:</strong> Son un tipo de cookies diseñadas para recabar y almacenar datos mientras el usuario accede a una página web...</li>
    <li><strong>Persistentes:</strong> Son un tipo de cookies en el que los datos siguen almacenados en el terminal y pueden ser accedidos y tratados...</li>
  </ol>

